package com.llmhub.audit;

/**
 * 감사 대상 대화가 어떻게 끝났는지 (R-5).
 *
 * <p>감사는 완료뿐 아니라 취소·오류에도 남는다. 근거는 첫 토큰보다 먼저 전달되므로(S6), 사용자가 그 뒤 스트림을
 * 취소하거나 게이트웨이가 실패해도 근거는 이미 브라우저에 갔다. "채팅 1회 → 감사 1건"을 지키려면 그 접근도 기록해야
 * 하고, 감사자는 answer가 잘렸는지 알아야 한다.
 */
public enum AuditOutcome {
	/** 응답이 끝까지 스트리밍됐다. */
	COMPLETE,
	/** 클라이언트가 중간에 끊었다(탭 닫기 등). answer는 부분이다. */
	CANCELLED,
	/** 스트림이 오류로 끝났다. answer는 부분이거나 비어 있다. */
	ERROR
}
